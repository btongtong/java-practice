package ch2;

public class Ex01_Variable1 {
    public static void main(String[] args) {
        int year = 0;
        int age = 25;

        System.out.println(year);
        System.out.println(age);

        year = age + 2000;
        age = age + 1;

        System.out.println(year);
        System.out.println(age);
    }
}
